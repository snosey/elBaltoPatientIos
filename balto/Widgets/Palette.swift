import UIKit

extension UIColor {
    
    static var offWhite: UIColor {
        
        get {
            
            return UIColor(red: 0xf2/0xff, green: 0xf2/0xff, blue: 0xf2/0xff, alpha: 1)
        }
    }
    
    static var grayLight: UIColor {
        
        get {
            
            return UIColor(red: 0xcb/0xff, green: 0xcb/0xff, blue: 0xcb/0xff, alpha: 1)
        }
    }
    
    static var gray: UIColor {
        
        get {
            
            return UIColor(red: 0xB8/0xff, green: 0xB8/0xff, blue: 0xB8/0xff, alpha: 1)
        }
    }
    
    static var green: UIColor {
        
        get {
            
            return UIColor(red: 0x31/0xff, green: 0x9d/0xff, blue: 0xac/0xff, alpha: 1)
        }
    }
    
    static var grayDark: UIColor {
        
        get {
            
            return UIColor(red: 0x52/0xff, green: 0x52/0xff, blue: 0x52/0xff, alpha: 1)
        }
    }
    
    static var pink: UIColor {
        
        get {
            
            return UIColor(red: 0xf3/0xff, green: 0x5b/0xff, blue: 0x66/0xff, alpha: 1)
        }
    }
    
    static var cyan: UIColor {
        
        get {
            
            return UIColor(red: 0x2F/0xff, green: 0x9C/0xff, blue: 0xAB/0xff, alpha: 1)
        }
    }
}
